# You can slice a string using (variable[first index:second index])
favorite_fruit = 'blueberry'
print(favorite_fruit)
# => 'blueberry'
print(favorite_fruit[3:8])
# => 'eberr'
print(favorite_fruit[:4])
# => 'blue'
print (favorite_fruit[4:])
# => 'berry'

